    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        int red = tblKlijenti.getSelectedRow();
        
        if(red != -1){
            ModelTabeleKlijenti mtk = (ModelTabeleKlijenti) tblKlijenti.getModel();
            Klijent odabraniKlijent = mtk.vratiKlijenta(red);
            srediFormuZaIzmenu(odabraniKlijent);
        }else{
            JOptionPane.showMessageDialog(this, "Morate odabrati red");
        }
    }//GEN-LAST:event_btnDetaljiActionPerformed


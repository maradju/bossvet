    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        boolean validnoPrazno = FormValidator.getInstance().validateEmpty(txtAdresa,txtBrojTelefona,txtEmailKlijenta,txtNazivKlijenta,txtPib);
        if(!validnoPrazno){
            JOptionPane.showMessageDialog(this, FormValidator.getInstance().getValidationMessage());
            return;
        }
        
        if(!FormValidator.getInstance().validateEmail(txtEmailKlijenta)){
            JOptionPane.showMessageDialog(this, FormValidator.getInstance().getValidationMessage());
            return;
        }
        
        String naziv = txtNazivKlijenta.getText();
        String adresa = txtAdresa.getText();
        String brojTelefona = txtBrojTelefona.getText();
        String emailKlijenta = txtEmailKlijenta.getText();
        try {
            int pib = Integer.parseInt(txtPib.getText());
            Klijent klijent = new Klijent(pib, naziv, adresa, brojTelefona, emailKlijenta);
            ServerskiTransferObjekat sto = Kontroler.getInstance().posaljiZahtevIPrimiOdgovor(Konstante.IZMENI_KLIJENTA, klijent);
            JOptionPane.showMessageDialog(this, sto.getPoruka());
            if(sto.isUspesno()){
                srediFormu();
                pretrazi();
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Pib mora biti broj");
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed


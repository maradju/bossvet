    private void srediFormu() {
        txtAdresa.setText("");
        txtPib.setText("");
        txtBrojTelefona.setText("");
        txtEmailKlijenta.setText("");
        txtNazivKlijenta.setText("");
        
        txtAdresa.setBorder(BorderFactory.createLineBorder(Color.black,1,true));
        txtPib.setBorder(BorderFactory.createLineBorder(Color.black,1,true));
        txtBrojTelefona.setBorder(BorderFactory.createLineBorder(Color.black,1,true));
        txtEmailKlijenta.setBorder(BorderFactory.createLineBorder(Color.black,1,true));
        txtNazivKlijenta.setBorder(BorderFactory.createLineBorder(Color.black,1,true));
        
        txtPib.setEditable(true);
        
        btnIzmeni.setEnabled(false);
        btnObrisi.setEnabled(false);
        btnSacuvaj.setEnabled(true);
    }

    private void pretrazi() {
        String pretraga = txtPretraga.getText();
        Klijent klijent = new Klijent();
        klijent.setNazivKlijenta(pretraga);
        
        ServerskiTransferObjekat sto = Kontroler.getInstance().posaljiZahtevIPrimiOdgovor(Konstante.PRETRAZI_KLIJENTA, klijent);
        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) sto.getOdgovor();
        
        ModelTabeleKlijenti mt = (ModelTabeleKlijenti) tblKlijenti.getModel();
        mt.setListaKlijenata(lista);
    }

    private void srediFormuZaIzmenu(Klijent odabraniKlijent) {
        txtAdresa.setText(odabraniKlijent.getAdresa());
        txtPib.setText(odabraniKlijent.getPib()+"");
        txtBrojTelefona.setText(odabraniKlijent.getBrojTelefona());
        txtEmailKlijenta.setText(odabraniKlijent.getEmailKlijenta());
        txtNazivKlijenta.setText(odabraniKlijent.getNazivKlijenta());
        
        txtPib.setEditable(false);
        
        btnIzmeni.setEnabled(true);
        btnObrisi.setEnabled(true);
        btnSacuvaj.setEnabled(false);
    }

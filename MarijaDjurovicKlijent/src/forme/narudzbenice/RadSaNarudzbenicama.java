    private void ucitajKlijente() {
        Klijent klijent = new Klijent();
        klijent.setNazivKlijenta("");

        ServerskiTransferObjekat sto = Kontroler.getInstance().posaljiZahtevIPrimiOdgovor(Konstante.PRETRAZI_KLIJENTA, klijent);

        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) sto.getOdgovor();
        cmbKlijent.removeAllItems();
        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbKlijent.addItem(opstiDomenskiObjekat);
        }
    }

    private void ucitajZaposlene() {

        Zaposleni zap = new Zaposleni();

        ServerskiTransferObjekat sto = Kontroler.getInstance().posaljiZahtevIPrimiOdgovor(Konstante.PRETRAGA_ZAPOSLENIH, zap);

        List<OpstiDomenskiObjekat> lista = (List<OpstiDomenskiObjekat>) sto.getOdgovor();
        cmbZaposleni.removeAllItems();
        for (OpstiDomenskiObjekat opstiDomenskiObjekat : lista) {
            cmbZaposleni.addItem(opstiDomenskiObjekat);
        }
    }

    void dodajStavku(StavkaNarudzbenice stavka) {
        ModelTabeleStavki mts = (ModelTabeleStavki) tabelaStavke.getModel();
        mts.dodajStavku(stavka);
        popuniUkupno(mts.getListaStavki());
    }

    private void popuniUkupno(List<OpstiDomenskiObjekat> listaStavki) {
        double suma = 0.00;
        for (OpstiDomenskiObjekat opstiDomenskiObjekat : listaStavki) {
            StavkaNarudzbenice stavka = (StavkaNarudzbenice) opstiDomenskiObjekat;
            suma += stavka.getVrednost();
        }

        txtUkupno.setText(suma + "");
    }

    public void obrisiStavku() {

        int red = tabelaStavke.getSelectedRow();

        if (red != 1) {
            ModelTabeleStavki mts = (ModelTabeleStavki) tabelaStavke.getModel();
            mts.obrisiStavku(red);
            popuniUkupno(mts.getListaStavki());
        }
    }

    private void pretrazi() {
        String pretaga = txtPretraga.getText();
        Klijent k = new Klijent();
        k.setNazivKlijenta(pretaga);

        Narudzbenica nar = new Narudzbenica();
        nar.setKlijent(k);

        ServerskiTransferObjekat sto = Kontroler.getInstance().posaljiZahtevIPrimiOdgovor(Konstante.PRETRAGA_NARUDZBENICE, nar);

        List<OpstiDomenskiObjekat> listaNarudzbenica = (List<OpstiDomenskiObjekat>) sto.getOdgovor();
        ModelTabeleNarudzbenica mtn = (ModelTabeleNarudzbenica) tblNarudzbenice.getModel();
        mtn.setListaNarudzbenica(listaNarudzbenica);
    }

    public void srediFormu() {
        btnIzmeni.setEnabled(false);
        btnObrisi.setEnabled(false);
        btnSacuvaj.setEnabled(true);
    }

    private void srediFormuZaIzmenu(Narudzbenica nar) {
        txtDatum.setText(new SimpleDateFormat("dd.MM.yyyy").format(nar.getDatum()));
        cmbKlijent.setSelectedItem(nar.getKlijent());
        cmbZaposleni.setSelectedItem(nar.getZaposleni());
        txtUkupno.setText(nar.getUkupno() + "");
        txtNarudzbenicaID.setText(nar.getNarudzbenicaID() + "");

        ModelTabeleStavki mts = (ModelTabeleStavki) tabelaStavke.getModel();
        mts.setListaStavki(nar.getListaStavki());

        btnIzmeni.setEnabled(true);
        btnObrisi.setEnabled(true);
        btnSacuvaj.setEnabled(false);

    }

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        if (!FormValidator.getInstance().validateEmpty(txtDatum)) {
            JOptionPane.showMessageDialog(this, FormValidator.getInstance().getValidationMessage());
            return;
        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");

        try {
            Date datum = sdf.parse(txtDatum.getText());
            ModelTabeleStavki mts = (ModelTabeleStavki) tabelaStavke.getModel();
            List<OpstiDomenskiObjekat> listaStavki = mts.getListaStavki();

            Zaposleni zap = (Zaposleni) cmbZaposleni.getSelectedItem();
            Klijent k = (Klijent) cmbKlijent.getSelectedItem();

            double ukupno = Double.parseDouble(txtUkupno.getText());

            Narudzbenica nar = new Narudzbenica(-1, datum, ukupno, k, zap, Kontroler.getInstance().getUlogovaniNalog(), listaStavki);

            ServerskiTransferObjekat sto = Kontroler.getInstance().posaljiZahtevIPrimiOdgovor(Konstante.SACUVAJ_NARUDZBENICU, nar);

            JOptionPane.showMessageDialog(this, sto.getPoruka());

            if (sto.isUspesno()) {
                pretrazi();
                srediFormu();
            }

        } catch (ParseException ex) {
            Logger.getLogger(RadSaNarudzbenicama.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

